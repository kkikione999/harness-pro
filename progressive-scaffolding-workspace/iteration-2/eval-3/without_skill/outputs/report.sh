#!/bin/bash
# report.sh - Generate evaluation summary report
# Usage: ./report.sh [markdown|plain]

EVAL_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
LOG_DIR="$EVAL_ROOT/logs"
REPORT_FILE="$EVAL_ROOT/evaluation-report.md"

mkdir -p "$LOG_DIR"

MODE="${1:-markdown}"

generate_report() {
    TIMESTAMP=$(date -Iseconds)
    EVAL_START="${EVAL_START:-$(date +%s)}"
    CURRENT_TIME=$(date +%s)
    DURATION=$((CURRENT_TIME - EVAL_START))

    # Collect file counts
    MD_COUNT=$(find "$EVAL_ROOT" -type f -name "*.md" 2>/dev/null | wc -l)
    JSON_COUNT=$(find "$EVAL_ROOT" -type f -name "*.json" 2>/dev/null | wc -l)
    LOG_COUNT=$(find "$EVAL_ROOT" -type f -name "*.log" 2>/dev/null | wc -l)
    SH_COUNT=$(find "$EVAL_ROOT" -type f -name "*.sh" 2>/dev/null | wc -l)

    # Recent logs
    RECENT_LOGS=""
    if [ -d "$LOG_DIR" ] && [ "$(ls -A "$LOG_DIR" 2>/dev/null)" ]; then
        RECENT_LOGS=$(tail -20 "$LOG_DIR"/*.log 2>/dev/null | head -50)
    fi

    case "$MODE" in
        markdown)
            cat << EOF
# Evaluation Report: iteration-2/eval-3/without_skill

**Generated**: $TIMESTAMP
**Duration**: ${DURATION}s
**Component**: progressive-scaffolding-workspace

## File Summary

| Type | Count |
|------|-------|
| Markdown | $MD_COUNT |
| JSON | $JSON_COUNT |
| Logs | $LOG_COUNT |
| Scripts | $SH_COUNT |

## Observability Scripts

- \`health.sh\` - Health check (JSON status)
- \`metrics.sh\` - Metrics collection
- \`trace.sh\` - Correlation ID injection
- \`report.sh\` - This report generator

## Recent Activity

\`\`\`
${RECENT_LOGS:-No recent activity}
\`\`\`

---
*Report generated by observability scripts*
EOF
            ;;
        plain)
            echo "=== Evaluation Report ==="
            echo "Generated: $TIMESTAMP"
            echo "Duration: ${DURATION}s"
            echo "Component: iteration-2/eval-3/without_skill"
            echo ""
            echo "File Summary:"
            echo "  Markdown: $MD_COUNT"
            echo "  JSON: $JSON_COUNT"
            echo "  Logs: $LOG_COUNT"
            echo "  Scripts: $SH_COUNT"
            echo ""
            echo "Recent Activity:"
            echo "${RECENT_LOGS:-No recent activity}"
            ;;
    esac
}

generate_report

# Save to file
if [ "$MODE" = "markdown" ]; then
    generate_report > "$REPORT_FILE"
    echo ""
    echo "Report saved to: $REPORT_FILE"
fi